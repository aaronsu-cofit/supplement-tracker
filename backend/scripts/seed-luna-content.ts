// ── Build ContentItem records ──────────────────────────────────────────────────

interface ContentRecord {
  key: string;
  body: string;
  title?: string;
}

function buildRecords(typeKey: string, content: TypeContent): ContentRecord[] {
  const records: ContentRecord[] = [];

  // Day 0 welcome
  records.push({
    key: `luna_${typeKey}_day0_welcome`,
    title: `Luna 歡迎訊息`,
    body: content.welcome,
  });

  // Days 1-14
  for (let n = 1; n <= 14; n++) {
    const day = content.days[n];
    if (!day) continue;

    if (day.task) {
      records.push({
        key: `luna_${typeKey}_day${n}_task`,
        title: `Day ${n} 晚間任務`,
        body: day.task,
      });
    }
    if (day.sleep) {
      records.push({
        key: `luna_${typeKey}_day${n}_sleep`,
        title: `Day ${n} 睡前陪伴`,
        body: day.sleep,
      });
    }
    if (day.morning) {
      records.push({
        key: `luna_${typeKey}_day${n}_morning`,
        title: `Day ${n} 晨起回報`,
        body: day.morning,
      });
    }
  }

  // Day 7 review
  records.push({
    key: `luna_${typeKey}_day7_review`,
    title: `Day 7 中期報告`,
    body: content.day7_review,
  });

  // Day 14 complete
  records.push({
    key: `luna_${typeKey}_day14_complete`,
    title: `Day 14 完課`,
    body: content.day14_complete,
  });

  return records;
}

